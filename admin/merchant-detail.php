<script>
function updateFeePreview() {
    const type = document.getElementById('feeType').value;
    const val = parseFloat(document.getElementById('feeValue').value) || 0;
    const flat = parseFloat(document.getElementById('feeFlat').value) || 0;
    const amount = 100000;
    let fee = 0;
    if (type === 'percentage') fee = Math.round(amount * val / 100);
    else if (type === 'flat') fee = val;
    else fee = Math.round(amount * val / 100) + flat;
    document.getElementById('feePreview').textContent = 'Fee: Rp ' + fee.toLocaleString('id-ID');
    document.getElementById('feeNetPreview').textContent = 'Net merchant: Rp ' + (amount - fee).toLocaleString('id-ID') + ' dari Rp 100.000';
}
updateFeePreview();
</script>


