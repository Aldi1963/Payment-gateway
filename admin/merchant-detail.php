// Handle POST actions
if (is_post()) {
    Auth::verifyCsrf();
    $action = $_POST['_action'] ?? '';

    if ($action === 'update_info') {
        $merchantRepo->update($merchantId, [
            'business_name' => sanitize($_POST['business_name'] ?? ''),
            'owner_name' => sanitize($_POST['owner_name'] ?? ''),
            'email' => sanitize($_POST['email'] ?? ''),
            'phone' => sanitize($_POST['phone'] ?? ''),
            'address' => sanitize($_POST['address'] ?? ''),
            'city' => sanitize($_POST['city'] ?? ''),
            'website' => sanitize($_POST['website'] ?? ''),
            'business_type' => sanitize($_POST['business_type'] ?? ''),
            'updated_at' => now(),
        ]);
        $auditService->log(Auth::id(), Auth::role(), $merchantId, 'merchant_updated', "Merchant info updated", []);
        flash('success', 'Info merchant berhasil diperbarui.');


    } elseif ($action === 'update_status') {
        $newStatus = sanitize($_POST['new_status'] ?? '');
        if (in_array($newStatus, ['pending','active','suspended','rejected'])) {
            $merchantRepo->update($merchantId, ['status' => $newStatus, 'updated_at' => now()]);
            $auditService->log(Auth::id(), Auth::role(), $merchantId, 'merchant_status_changed', "Status changed to {$newStatus}", ['old' => $merchant['status'], 'new' => $newStatus]);
            flash('success', 'Status merchant diperbarui.');
        }

    } elseif ($action === 'update_fee') {
        $merchantRepo->update($merchantId, [
            'fee_type' => sanitize($_POST['fee_type'] ?? 'percentage'),
            'fee_value' => (float)($_POST['fee_value'] ?? 0),
            'fee_flat' => (float)($_POST['fee_flat'] ?? 0),
            'updated_at' => now(),
        ]);
        $auditService->log(Auth::id(), Auth::role(), $merchantId, 'merchant_fee_updated', "Fee updated", $_POST);
        flash('success', 'Fee merchant diperbarui.');

    } elseif ($action === 'update_urls') {
        $merchantRepo->update($merchantId, [
            'webhook_url' => sanitize($_POST['webhook_url'] ?? ''),
            'redirect_url' => sanitize($_POST['redirect_url'] ?? ''),
            'updated_at' => now(),
        ]);
        flash('success', 'URL merchant diperbarui.');

    } elseif ($action === 'regenerate_key') {
        $newKey = $merchantRepo->regenerateApiKey($merchantId);
        $auditService->log(Auth::id(), Auth::role(), $merchantId, 'api_key_regenerated', "API key regenerated by admin", []);
        flash('success', 'API key berhasil di-regenerate.');

    } elseif ($action === 'add_staff') {
        $staffEmail = sanitize($_POST['staff_email'] ?? '');
        $staffName = sanitize($_POST['staff_name'] ?? '');
        $staffPassword = $_POST['staff_password'] ?? '';
        $staffPermissions = $_POST['permissions'] ?? [];

        if (empty($staffName) || !is_valid_email($staffEmail) || strlen($staffPassword) < 8) {
            flash('error', 'Data staff tidak valid. Nama, email valid, dan password min 8 karakter.');
        } elseif ($userRepo->findByEmail($staffEmail)) {
            flash('error', 'Email sudah terdaftar.');
        } else {
            $userRepo->create([
                'id' => generate_uuid(),
                'merchant_id' => $merchantId,
                'name' => $staffName,
                'email' => $staffEmail,
                'password_hash' => password_hash($staffPassword, PASSWORD_DEFAULT),
                'role' => 'staff_merchant',
                'status' => 'active',
                'permissions' => $staffPermissions,
                'created_at' => now(),
                'updated_at' => now(),
            ]);
            $auditService->log(Auth::id(), Auth::role(), $merchantId, 'staff_added', "Staff {$staffName} added", ['email' => $staffEmail]);
            flash('success', "Staff {$staffName} berhasil ditambahkan.");
        }

    } elseif ($action === 'remove_staff') {
        $staffId = $_POST['staff_id'] ?? '';
        $staff = $userRepo->find($staffId);
        if ($staff && $staff['merchant_id'] === $merchantId && $staff['role'] === 'staff_merchant') {
            $userRepo->delete($staffId);
            $auditService->log(Auth::id(), Auth::role(), $merchantId, 'staff_removed', "Staff {$staff['name']} removed", []);
            flash('success', "Staff berhasil dihapus.");
        }
    }
    redirect("/admin/merchant-detail.php?id={$merchantId}&tab=" . ($_POST['_tab'] ?? 'info'));
}

